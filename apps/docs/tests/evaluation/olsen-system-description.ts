export const SYSTEM_DESCRIPTION = `
## Malleable Forms — System Description

Malleable Forms is an open-source web toolkit for conversational form creation. Creators describe their data collection needs in natural language and iteratively refine the form schema through a structured conversational process.

### 1. Editor
A Markdown editor where the creator writes a form description using four sections: Purpose, Audience, Constraints, and Exclusions. The editor accepts free-form natural language.

### 2. Field Types
The schema engine supports eight field types: text, number, select, date, boolean, file, scale, and nested group. Each field carries type-specific metadata (e.g., value ranges, option lists). Fields are generated by an LLM; the creator does not configure types directly.

### 3. Form Generation
An LLM generates a form schema from the natural language intent, inferring field types, labels, descriptions, and validation rules. Output is validated against a structured JSON schema. Generation is non-deterministic: different models or prompts may yield different schemas for the same intent.

### 4. Refinement
After generation, the system produces 3–5 refinement questions ("design probes") with 2–4 clickable options each. Selecting an option updates both the intent and the schema via an incremental patch. Follow-up probes may be generated (0–3 per resolution). Probes can be dismissed. A separate dialog lets collaborators type free-text requirements that are translated into probes.

### 5. Pipeline Strategy
When the creator edits the intent, a delta computation selects a regeneration strategy: full generation if core sections changed, deterministic field filtering if only exclusions changed, constraint recheck if only constraints changed, or no action.

### 6. Standards Detection
Keyword matching against a curated registry (FHIR, ESRS E1, GS1 GTIN, Schema.org JobPosting) detects relevant domain standards. Detection is deterministic. Detected standards are presented as probes; accepting one injects its field constraints into subsequent generation.

### 7. Conflict Detection
After schema updates, the system checks for duplicate fields, type mismatches, semantic overlap, and naming inconsistencies. Deterministic checks handle structural issues; LLM-based analysis handles semantic issues. Detected conflicts are presented with fix options.

### 8. Validation
Runtime validation converts the schema into type-safe rules (Zod). Per-field constraints include regex patterns, numeric ranges, and required flags. Cross-field validation is not supported.

### 9. Data Collection
Published forms collect responses as JSON matching the schema at submission time. Responses are stored in a database. Schema evolution does not require data migration.

### 10. Change Tracking
An append-only provenance log records every schema change with the action, affected layer, a structured diff, an optional rationale, and previous-state snapshots. The log is browsable from the workspace.
`.trim();


