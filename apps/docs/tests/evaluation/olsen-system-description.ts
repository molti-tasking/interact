export const GOOGLE_FORMS_DESCRIPTION = `
## Google Forms — System Description

Google Forms is a free, web-based form builder by Google, widely used for surveys, registrations, and data collection.

### 1. Editor
A single-page WYSIWYG editor where the creator adds questions one at a time. Each question is configured individually: choose a type, type the question text, and optionally add description text.

### 2. Field Types
Supports short answer, paragraph, multiple choice, checkboxes, dropdown, file upload, linear scale, multiple choice grid, checkbox grid, date, and time.

### 3. Form Generation
The creator manually adds and configures each question. A gallery of ~20 pre-built templates (Contact Information, RSVP, Event Registration, Customer Feedback, etc.) provides starting points that are customized field by field.

### 4. Refinement
The creator edits questions directly in the WYSIWYG editor. Changes are immediate and visible. Questions can be reordered via drag-and-drop, duplicated, or deleted.

### 5. Logic and Branching
Section-based branching: "Go to section X based on answer." Branching is configured per question for multiple-choice and dropdown types.

### 6. Validation
Per-field validation for text fields: number, text, length, and regex constraints. Required flag available for all question types.

### 7. Collaboration
Real-time collaborative editing. Multiple editors can work simultaneously. Comment threads on individual questions.

### 8. Data Collection
Responses stored in a linked Google Sheet. Summary charts auto-generated. Response notifications available.

### 9. Change Tracking
Edit history available through Google's version history (same as Google Docs). No structured diff or rationale capture.
`.trim();


export const AIRTABLE_DESCRIPTION = `
## Airtable — System Description

Airtable is a cloud-based platform combining spreadsheet and database features for structured data collection and management.

### 1. Editor
A spreadsheet-like interface where each column is a typed field and each row is a record. The creator adds fields by clicking "+" and selecting a type. Fields are configured individually with names, types, and options.

### 2. Field Types
Rich type system with ~25 types: single line text, long text, attachment, checkbox, multiple select, single select, collaborator, date, phone, email, URL, number, currency, percent, duration, rating, formula, rollup, count, lookup, created time, last modified time, auto-number, barcode, and button.

### 3. Form Generation
A form view is auto-generated from the table's field definitions. The creator can reorder fields, add descriptions, mark fields as required, and add cover images. An extensive template gallery (~100+ templates) organized by category provides pre-built tables with fields, views, and sample data.

### 4. Refinement
The creator edits fields directly in the table editor. Fields can be added, renamed, retyped, or deleted. The form view updates to reflect table changes.

### 5. Relationships and Lookups
Tables link to other tables via linked record fields. Lookup, rollup, and count fields reference linked records, enabling relational data modeling.

### 6. Validation
Field-level type enforcement (e.g., email fields validate format). Formula fields support computed values. Select fields constrain input to predefined options.

### 7. Automations
Trigger-action automations: when a record matches conditions, send email, update record, run script, or call webhook.

### 8. Data Collection
Responses collected through the form view or direct table entry. Multiple views of the same data: grid, form, calendar, gallery, kanban, timeline, Gantt. An interface designer supports custom dashboards and detail views.

### 9. Change Tracking
Record-level revision history shows who changed what and when. Field-level change tracking available for some field types. No structured schema-level provenance log.
`.trim();
